"""
Calculator library
"""


def add(a, b):
    """Returns the sum of a and b."""
    return a + b


def subtract(a, b):
    """Returns the subtraction of b from a."""
    return a - b


def multiply(a, b):
    """Returns the multiplication of a and b."""
    return a * b


def divide(a, b):
    """Returns the division of a by b."""
    return a / b


def power(a, b):
    """Returns a raised to the power of b."""
    return a**b
